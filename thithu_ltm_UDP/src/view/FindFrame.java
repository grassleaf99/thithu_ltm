    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Nhap ten sinh vien");
            return;
        }
        else
        {
            clientController.sendData(jTextField1.getText());
            this.dsSV = clientController.readData();
            this.dtmSV.setRowCount(this.dsSV.size());
            for(int i = 0; i < this.dsSV.size(); i++)
            {
                SV sv = this.dsSV.get(i);
                dtmSV.setValueAt(sv.getId(), i, 0);
                dtmSV.setValueAt(sv.getTen(), i, 1);
                dtmSV.setValueAt(sv.getDob(), i, 2);
                dtmSV.setValueAt(sv.getKhoa(), i, 3);
                dtmSV.setValueAt(sv.getQue(), i, 4);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if((row < 0) || (row >= this.dtmSV.getRowCount()))
        {
            JOptionPane.showMessageDialog(this, "Chon sinh vien de sua");
            return;
        }
        else
        {
            SV sv = this.dsSV.get(row);
            new EditFrame(sv, this.clientController).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

